
//What are Events in JavaScript?
  //In JavaScript, events are actions or occurrences that happen in the browser, which the browser then notifies your code about so it can respond to them. Events can be triggered by user interactions (like clicking a button, typing in a text field, or moving the mouse), by the browser itself (like when a page finishes loading), or by APIs.

  //Common Types of Events
    //Mouse Events: click, dblclick, mouseover, mouseout, mousemove, mousedown, mouseup
    //Keyboard Events: keydown, keypress, keyup
    //Form Events: submit, change, focus, blur
    //Window Events: load, resize, scroll, unload
    //Document Events: DOMContentLoaded, readystatechange
    //Touch Events: touchstart, touchmove, touchend

  //Events in JavaScript are fundamental to creating interactive, dynamic, and responsive web applications.